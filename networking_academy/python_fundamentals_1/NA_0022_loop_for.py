# Función range con un argumento,
# el cual indica el límite de la variable de control.
for i in range(10):
    print("El valor de i es", i)

# Función range con dos argumentos,
# El primero indica el inicio de la variable de control,
# El segundo indica el límite de esta misma.
for i in range(2, 8):
    print("El valor de i es", i)

# Función range con tres argumentos
# El primero indica el inicio de la variable de control,
# El segundo indica el límite de esta misma.
# El tercero indica el valor de incremento de la variable.
for i in range(2, 8, 3):
    print("El valor de i es", i)
